<P>The Systems Development Life Cycle (SDLC) phases serve as a programmatic 
guide to project activity and provide a flexible but consistent way to conduct 
projects to a depth matching the scope of the project. Each of the SDLC phase 
objectives are described in this section with key deliverables, a description of 
recommended tasks, and a summary of related control objectives for effective 
management. It is critical for the project manager to establish and monitor 
control objectives during each SDLC phase while executing projects. Control 
objectives help to provide a clear statement of the desired result or purpose 
and should be used throughout the entire SDLC process. Control objectives can be 
grouped into major categories (Domains), and relate to the SDLC phases as shown 
in the figure.<SUP class=reference id=cite_ref-USHR99_8-1><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-USHR99-8"><SPAN>[</SPAN>9<SPAN>]</SPAN></A></SUP></P>
<P>To manage and control any SDLC initiative, each project will be required to 
establish some degree of a <A class=mw-redirect title="Work Breakdown Structure" 
href="http://en.wikipedia.org/wiki/Work_Breakdown_Structure">Work Breakdown 
Structure</A> (WBS) to capture and schedule the work necessary to complete the 
project. The WBS and all programmatic material should be kept in the “Project 
Description” section of the project notebook. The WBS format is mostly left to 
the project manager to establish in a way that best describes the project work. 
There are some key areas that must be defined in the WBS as part of the SDLC 
policy. The following diagram describes three key areas that will be addressed 
in the WBS in a manner established by the project manager.<SUP class=reference 
id=cite_ref-USHR99_8-2><A 
