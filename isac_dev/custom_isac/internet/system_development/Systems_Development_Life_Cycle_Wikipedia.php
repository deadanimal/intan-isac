<P>An alternative to the SDLC is <A class=mw-redirect 
title="Rapid Application Development" 
href="http://en.wikipedia.org/wiki/Rapid_Application_Development">Rapid 
Application Development</A>, which combines prototyping, Joint Application 
Development and implementation of CASE tools. The advantages of RAD are speed, 
reduced development cost, and active user involvement in the development 
process.</P>
<P>It should not be assumed that just because the waterfall model is the oldest 
original SDLC model that it is the most efficient system. At one time the model 
was beneficial mostly to the world of automating activities that were assigned 
to clerks and accountants. However, the world of technological evolution is 
demanding that systems have a greater functionality that would assist help desk 
technicians/administrators or information technology specialists/analysts.</P>
