<P>The <B>Systems Development Life Cycle (SDLC)</B>, or <I>Software Development 
Life Cycle</I> in <A title="Systems engineering" 
href="http://en.wikipedia.org/wiki/Systems_engineering">systems engineering</A> 
and <A title="Software engineering" 
href="http://en.wikipedia.org/wiki/Software_engineering">software 
engineering</A>, is the process of creating or altering systems, and the models 
and <A class=mw-redirect title=Methodologies 
href="http://en.wikipedia.org/wiki/Methodologies">methodologies</A> that people 
use to develop these systems. The concept generally refers to <A 
class=mw-redirect title="Computer systems" 
href="http://en.wikipedia.org/wiki/Computer_systems">computer</A> or <A 
title="Information system" 
href="http://en.wikipedia.org/wiki/Information_system">information 
systems</A>.</P>
<P>In software engineering the SDLC concept underpins many kinds of <A 
class=mw-redirect title="Software development methodologies" 
href="http://en.wikipedia.org/wiki/Software_development_methodologies">software 
development methodologies</A>. These methodologies form the framework for 
planning and controlling the creation of an information system<SUP 
class=reference id=cite_ref-0><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-0"><SPAN>[</SPAN>1<SPAN>]</SPAN></A></SUP>: 
the <A title="Software development process" 
href="http://en.wikipedia.org/wiki/Software_development_process">software 
development process</A>.</P>
