<SPAN class=mw-headline id=Baselines_in_the_SDLC>Baselines in the 
SDLC</SPAN></H3>
<P>Baselines are an important part of the Systems Development Life Cycle (SDLC). 
These baselines are established after four of the five phases of the SDLC and 
are critical to the iterative nature of the model <SUP class=reference 
id=cite_ref-9><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-9"><SPAN>[</SPAN>10<SPAN>]</SPAN></A></SUP>. 
Each baseline is considered as a milestone in the SDLC.</P>
<UL>
  <LI>Functional Baseline: established after the conceptual design phase. 
  <LI>Allocated Baseline: established after the preliminary design phase. 
  <LI>Product Baseline: established after the detail design and development 
  phase. 
  <LI>Updated Product Baseline: established after the production construction 
  phase. </LI></UL>
