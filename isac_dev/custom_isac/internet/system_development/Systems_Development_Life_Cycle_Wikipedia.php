<P>The upper section of the <A class=mw-redirect 
title="Work Breakdown Structure" 
href="http://en.wikipedia.org/wiki/Work_Breakdown_Structure">Work Breakdown 
Structure</A> (WBS) should identify the major phases and milestones of the 
project in a summary fashion. In addition, the upper section should provide an 
overview of the full scope and timeline of the project and will be part of the 
initial project description effort leading to project approval. The middle 
section of the WBS is based on the seven Systems Development Life Cycle (SDLC) 
phases as a guide for WBS task development. The WBS elements should consist of 
milestones and “tasks” as opposed to “activities” and have a definitive period 
(usually two weeks or more). Each task must have a measurable output (e.g. 
document, decision, or analysis). A WBS task may rely on one or more activities 
(e.g. <A title="Software engineering" 
href="http://en.wikipedia.org/wiki/Software_engineering">software 
engineering</A>, <A title="Systems engineering" 
href="http://en.wikipedia.org/wiki/Systems_engineering">systems engineering</A>) 
and may require close coordination with other tasks, either internal or external 
to the project. Any part of the project needing support from contractors should 
have a <A title="Statement of work" 
href="http://en.wikipedia.org/wiki/Statement_of_work">Statement of work</A> 
(SOW) written to include the appropriate tasks from the SDLC phases. The 
development of a SOW does not occur during a specific phase of SDLC but is 
developed to include the work from the SDLC process that may be conducted by 
external resources such as contractors.<SUP class=reference 
id=cite_ref-USHR99_8-4><A 
