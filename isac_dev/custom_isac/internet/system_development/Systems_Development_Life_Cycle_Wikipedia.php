<H2><SPAN class=editsection>[<A title="Edit section: Overview" 
href="http://en.wikipedia.org/w/index.php?title=Systems_Development_Life_Cycle&amp;action=edit&amp;section=1">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Overview>Overview</SPAN></H2>
<P>Systems Development Life Cycle (SDLC) is a logical process used by a <A 
title="Systems analyst" 
href="http://en.wikipedia.org/wiki/Systems_analyst">systems analyst</A> to 
develop an <A title="Information system" 
href="http://en.wikipedia.org/wiki/Information_system">information system</A>, 
including <A class=mw-redirect title=Requirements 
href="http://en.wikipedia.org/wiki/Requirements">requirements</A>, <A 
title=Validation href="http://en.wikipedia.org/wiki/Validation">validation</A>, 
<A title=Training href="http://en.wikipedia.org/wiki/Training">training</A>, and 
user (stakeholder) ownership. Any SDLC should result in a high quality system 
that meets or exceeds customer expectations, reaches completion within time and 
cost estimates, works effectively and efficiently in the current and planned <A 
class=mw-redirect title="Information Technology" 
href="http://en.wikipedia.org/wiki/Information_Technology">Information 
Technology</A> <A title=Infrastructure 
href="http://en.wikipedia.org/wiki/Infrastructure">infrastructure</A>, and is 
inexpensive to maintain and cost-effective to enhance.<SUP class=reference 
id=cite_ref-1><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-1"><SPAN>[</SPAN>2<SPAN>]</SPAN></A></SUP></P>
<P>Computer systems are complex and often (especially with the recent rise of <A 
class=mw-redirect title="Service-Oriented Architecture" 
href="http://en.wikipedia.org/wiki/Service-Oriented_Architecture">Service-Oriented 
Architecture</A>) link multiple traditional systems potentially supplied by 
different software vendors. To manage this level of complexity, a number of 
systems development life cycle (SDLC) models have been created: "<A 
title="Waterfall model" 
href="http://en.wikipedia.org/wiki/Waterfall_model">waterfall</A>"; "fountain"; 
"<A title="Spiral model" 
href="http://en.wikipedia.org/wiki/Spiral_model">spiral</A>"; "build and fix"; 
"<A title="Software prototyping" 
href="http://en.wikipedia.org/wiki/Software_prototyping#Throwaway_prototyping">rapid 
prototyping</A>"; "<A class=mw-redirect title="Incremental development" 
href="http://en.wikipedia.org/wiki/Incremental_development">incremental</A>"; 
and "synchronize and stabilize".<SUP class=Template-Fact 
title="This claim needs references to reliable sources from December 2009" 
style="WHITE-SPACE: nowrap">[<I><A title="Wikipedia:Citation needed" 
href="http://en.wikipedia.org/wiki/Wikipedia:Citation_needed">citation 
needed</A></I>]</SUP></P>
<P>SDLC models can be described along a spectrum of agile to iterative to 
sequential. <A title="Agile software development" 
href="http://en.wikipedia.org/wiki/Agile_software_development">Agile 
methodologies</A>, such as <A title="Extreme Programming" 
href="http://en.wikipedia.org/wiki/Extreme_Programming">XP</A> and <A 
title="Scrum (development)" 
href="http://en.wikipedia.org/wiki/Scrum_(development)">Scrum</A>, focus on 
light-weight processes which allow for rapid changes along the development 
cycle. <A title="Iterative and incremental development" 
href="http://en.wikipedia.org/wiki/Iterative_and_incremental_development">Iterative</A> 
methodologies, such as <A class=mw-redirect title="Rational Unified Process" 
href="http://en.wikipedia.org/wiki/Rational_Unified_Process">Rational Unified 
Process</A> and <A title="Dynamic Systems Development Method" 
href="http://en.wikipedia.org/wiki/Dynamic_Systems_Development_Method">Dynamic 
Systems Development Method</A>, focus on limited project scopes and expanding or 
improving products by multiple iterations. Sequential or big-design-upfront 
(BDUF) models, such as <A title="Waterfall model" 
href="http://en.wikipedia.org/wiki/Waterfall_model">Waterfall</A>, focus on 
complete and correct planning to guide large projects and risks to successful 
and predictable results.<SUP class=Template-Fact 
title="This claim needs references to reliable sources from December 2009" 
style="WHITE-SPACE: nowrap">[<I><A title="Wikipedia:Citation needed" 
href="http://en.wikipedia.org/wiki/Wikipedia:Citation_needed">citation 
needed</A></I>]</SUP></P>
<P>Some agile and iterative proponents confuse the term SDLC with sequential or 
"more traditional" processes; however, SDLC is an umbrella term for all 
methodologies for the design, implementation, and release of software.<SUP 
class=reference id=cite_ref-2><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-2"><SPAN>[</SPAN>3<SPAN>]</SPAN></A></SUP><SUP 
class=reference id=cite_ref-3><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-3"><SPAN>[</SPAN>4<SPAN>]</SPAN></A></SUP></P>
<P>In <A title="Project management" 
href="http://en.wikipedia.org/wiki/Project_management">project management</A> a 
project can be defined both with a <A class=mw-redirect 
title="Project life cycle" 
href="http://en.wikipedia.org/wiki/Project_life_cycle">project life cycle</A> 
(PLC) and an SDLC, during which slightly different activities occur. According 
to Taylor (2004) "the project life cycle encompasses all the activities of the 
<A title=Project href="http://en.wikipedia.org/wiki/Project">project</A>, while 
the systems development life cycle focuses on realizing the product <A 
title=Requirement 
href="http://en.wikipedia.org/wiki/Requirement">requirements</A>".<SUP 
class=reference id=cite_ref-4><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-4"><SPAN>[</SPAN>5<SPAN>]</SPAN></A></SUP></P>
<H2><SPAN class=editsection>[<A title="Edit section: History" 
href="http://en.wikipedia.org/w/index.php?title=Systems_Development_Life_Cycle&amp;action=edit&amp;section=2">edit</A>]</SPAN> 
<SPAN class=mw-headline id=History>History</SPAN></H2>
<P>The systems development lifecycle (SDLC) is a type of methodology used to 
describe the process for building <A title="Information system" 
href="http://en.wikipedia.org/wiki/Information_system">information systems</A>, 
intended to develop information systems in a very deliberate, structured and 
methodical way, reiterating each stage of the <A title="Product lifecycle" 
href="http://en.wikipedia.org/wiki/Product_lifecycle">life cycle</A>. The 
systems development life cycle, according to Elliott &amp; Strachan &amp; 
Radford (2004), "originated in the 1960s to develop large scale functional <A 
class=mw-redirect title="Business system" 
href="http://en.wikipedia.org/wiki/Business_system">business systems</A> in an 
age of large scale <A class=mw-redirect title="Business conglomerate" 
href="http://en.wikipedia.org/wiki/Business_conglomerate">business 
conglomerates</A>. Information systems activities revolved around heavy <A 
class=mw-redirect title="Data processing" 
href="http://en.wikipedia.org/wiki/Data_processing">data processing</A> and <A 
class=mw-redirect title="Number crunching" 
href="http://en.wikipedia.org/wiki/Number_crunching">number crunching</A> 
routines".<SUP class=reference id=cite_ref-Ell04_5-0><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-Ell04-5"><SPAN>[</SPAN>6<SPAN>]</SPAN></A></SUP></P>
<P>Several systems development frameworks have been partly based on SDLC, such 
as the <A title="Structured Systems Analysis and Design Method" 
href="http://en.wikipedia.org/wiki/Structured_Systems_Analysis_and_Design_Method">Structured 
Systems Analysis and Design Method</A> (SSADM) produced for the <A 
class=mw-redirect title="UK government" 
href="http://en.wikipedia.org/wiki/UK_government">UK government</A> <A 
title="Office of Government Commerce" 
href="http://en.wikipedia.org/wiki/Office_of_Government_Commerce">Office of 
Government Commerce</A> in the 1980s. Ever since, according to Elliott (2004), 
"the traditional life cycle approaches to systems development have been 
increasingly replaced with alternative approaches and frameworks, which 
attempted to overcome some of the inherent deficiencies of the traditional 
SDLC".<SUP class=reference id=cite_ref-Ell04_5-1><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-Ell04-5"><SPAN>[</SPAN>6<SPAN>]</SPAN></A></SUP></P>
<H2><SPAN class=editsection>[<A title="Edit section: Systems development phases" 
href="http://en.wikipedia.org/w/index.php?title=Systems_Development_Life_Cycle&amp;action=edit&amp;section=3">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Systems_development_phases>Systems development 
phases</SPAN></H2>
<P>Systems Development Life Cycle (SDLC) adheres to important phases that are 
essential for developers, such as <A title=Planning 
href="http://en.wikipedia.org/wiki/Planning">planning</A>, <A title=Analysis 
href="http://en.wikipedia.org/wiki/Analysis">analysis</A>, <A title=Design 
href="http://en.wikipedia.org/wiki/Design">design</A>, and <A 
title=Implementation 
href="http://en.wikipedia.org/wiki/Implementation">implementation</A>, and are 
explained in the section below. There are several Systems Development Life Cycle 
Models in existence. The oldest model, that was originally regarded as "the 
Systems Development Life Cycle" is the <A title="Waterfall model" 
href="http://en.wikipedia.org/wiki/Waterfall_model">waterfall model</A>: a 
sequence of stages in which the output of each stage becomes the input for the 
next. These stages generally follow the same basic steps but many different 
waterfall methodologies give the steps different names and the number of steps 
seem to vary between 4 and 7. There is no definitively correct Systems 
Development Life Cycle model, but the steps can be characterized and divided in 
several steps.</P>
