<H3><SPAN class=editsection>[<A title="Edit section: Initiation/planning" 
href="http://en.wikipedia.org/w/index.php?title=Systems_Development_Life_Cycle&amp;action=edit&amp;section=4">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Initiation.2Fplanning>Initiation/planning</SPAN></H3>
<P>To generate a high-level view of the intended <A title=Project 
href="http://en.wikipedia.org/wiki/Project">project</A> and determine the <A 
title=Goal href="http://en.wikipedia.org/wiki/Goal">goals</A> of the project. 
The <A title="Feasibility study" 
href="http://en.wikipedia.org/wiki/Feasibility_study">feasibility study</A> is 
sometimes used to present the project to upper management in an attempt to gain 
funding. Projects are typically evaluated in three areas of feasibility: 
economical, operational or organizational, and technical. Furthermore, it is 
also used as a reference to keep the project on track and to evaluate the 
progress of the MIS team.<SUP class=reference id=cite_ref-7><A 
href="http://en.wikipedia.org/wiki/Systems_Development_Life_Cycle#cite_note-7"><SPAN>[</SPAN>8<SPAN>]</SPAN></A></SUP> 
The MIS is also a complement of those phases. This phase is also called the 
analysis phase.</P>
<H3><SPAN class=editsection>[<A 
title="Edit section: Requirements gathering and analysis" 
href="http://en.wikipedia.org/w/index.php?title=Systems_Development_Life_Cycle&amp;action=edit&amp;section=5">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Requirements_gathering_and_analysis>Requirements 
gathering and analysis</SPAN></H3>
<P>The goal of <A title="Systems analysis" 
href="http://en.wikipedia.org/wiki/Systems_analysis">systems analysis</A> is to 
determine where the problem is in an attempt to fix the system. This step 
involves <A title="Decomposition (computer science)" 
href="http://en.wikipedia.org/wiki/Decomposition_(computer_science)">breaking 
down</A> the system in different pieces and drawing <A title=Diagram 
href="http://en.wikipedia.org/wiki/Diagram">diagrams</A> to analyze the 
situation, analyzing project goals, breaking down what needs to be created and 
attempting to engage users so that definite <A title=Requirement 
href="http://en.wikipedia.org/wiki/Requirement">requirements</A> can be defined. 
Requirements Gathering sometimes requires individuals/teams from client as well 
as service provider sides to get detailed and accurate requirements.</P>
<H3><SPAN class=editsection>[<A title="Edit section: Design" 
href="http://en.wikipedia.org/w/index.php?title=Systems_Development_Life_Cycle&amp;action=edit&amp;section=6">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Design>Design</SPAN></H3>
<P>In <A title="Systems design" 
href="http://en.wikipedia.org/wiki/Systems_design">systems design</A> functions 
and operations are described in detail, including screen layouts, business 
rules, process diagrams and other documentation. The output of this stage will 
describe the new system as a collection of modules or <A class=mw-redirect 
title=Subsystem 
href="http://en.wikipedia.org/wiki/Subsystem">subsystems</A>.</P>
<P>The design stage takes as its initial input the requirements identified in 
the approved requirements document. For each requirement, a set of one or more 
design elements will be produced as a result of interviews, workshops, and/or 
prototype efforts. Design elements describe the desired software features in 
detail, and generally include functional hierarchy diagrams, screen layout 
diagrams, tables of business rules, business process diagrams, pseudocode, and a 
complete entity-relationship diagram with a full data dictionary. These design 
elements are intended to describe the software in sufficient detail that skilled 
programmers may develop the software with minimal additional input.</P>
