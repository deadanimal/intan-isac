src="Systems_Development_Life_Cycle_Wikipedia_files/magnify-clip.png" 
width=15></A></DIV>The SDLC can be divided into ten phases during which defined 
IT work products are created or modified. The tenth phase occurs when the system 
is disposed of and the task performed is either eliminated or transferred to 
other systems. The tasks and work products for each phase are described in 
subsequent chapters. Not every project will require that the phases be 
sequentially executed. However, the phases are interdependent. Depending upon 
the size and complexity of the project, phases may be combined or may 
overlap.<SUP class=reference id=cite_ref-6><A 
