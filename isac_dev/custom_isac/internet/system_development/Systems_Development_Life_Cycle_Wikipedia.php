<SPAN class=mw-headline id=Strengths_and_weaknesses>Strengths and 
weaknesses</SPAN></H2>
<P>Few people in the modern computing world would use a strict <A 
title="Waterfall model" 
href="http://en.wikipedia.org/wiki/Waterfall_model">waterfall model</A> for 
their Systems Development Life Cycle (SDLC) as many modern methodologies have 
superseded this thinking. Some will argue that the SDLC no longer applies to 
models like Agile computing, but it is still a term widely in use in Technology 
circles. The SDLC practice has advantages in traditional models of software 
development, that lends itself more to a structured environment. The 
disadvantages to using the SDLC methodology is when there is need for iterative 
development or (i.e. web development or e-commerce) where stakeholders need to 
review on a regular basis the software being designed. Instead of viewing SDLC 
from a strength or weakness perspective, it is far more important to take the 
best practices from the SDLC model and apply it to whatever may be most 
appropriate for the software being designed.</P>
