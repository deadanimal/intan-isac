<P>The <B>knowledge economy</B> is a term that refers either to an <B>economy of 
knowledge</B> focused on the production and management of knowledge in the frame 
of <A title=Economy href="http://en.wikipedia.org/wiki/Economy">economic</A> 
constraints, or to a <B>knowledge-based economy</B>. In the second meaning, more 
frequently used, it refers to the use of <A title=Knowledge 
href="http://en.wikipedia.org/wiki/Knowledge">knowledge</A> technologies (such 
as <A title="Knowledge engineering" 
href="http://en.wikipedia.org/wiki/Knowledge_engineering">knowledge 
engineering</A> and <A title="Knowledge management" 
href="http://en.wikipedia.org/wiki/Knowledge_management">knowledge 
management</A>) to produce <A class=mw-redirect title=Economic 
href="http://en.wikipedia.org/wiki/Economic">economic</A> benefits as well as 
job creation. The phrase was popularized if not invented by <A 
title="Peter Drucker" href="http://en.wikipedia.org/wiki/Peter_Drucker">Peter 
Drucker</A> as the title of Chapter 12 in his book <I><A class=new 
title="The Age of Discontinuity (page does not exist)" 
href="http://en.wikipedia.org/w/index.php?title=The_Age_of_Discontinuity&amp;action=edit&amp;redlink=1">The 
Age of Discontinuity</A></I><SUP class=reference id=cite_ref-0><A 
href="http://en.wikipedia.org/wiki/Knowledge_economy#cite_note-0"><SPAN>[</SPAN>1<SPAN>]</SPAN></A></SUP>.</P>
<P>The essential difference is that in a <I>knowledge economy</I>, knowledge is 
a product, in <I>knowledge-based economy</I>, knowledge is a tool. This 
difference is not yet well distinguished in the subject matter literature. They 
both are strongly interdisciplinary, involving economists, computer scientists, 
software engineers, mathematicians, chemists, physicists, as well as 
cognitivists, psychologists and sociologists.</P>
<P>Various observers describe today's global economy as one in transition to a 
"knowledge economy", as an extension of an "<A title="Information society" 
href="http://en.wikipedia.org/wiki/Information_society">information 
society</A>". The transition requires that the rules and practices that 
determined success in the industrial economy need rewriting in an 
interconnected, globalized economy where knowledge resources such as <A 
title="Trade secret" 
href="http://en.wikipedia.org/wiki/Trade_secret">know-how</A> and expertise are 
as critical as other economic resources. According to analysts of the "knowledge 
economy", these rules need to be rewritten at the levels of firms and industries 
in terms of knowledge management and at the level of public policy as knowledge 
policy or knowledge-related policy.<SUP class=Template-Fact 
title="This claim needs references to reliable sources from May 2008" 
