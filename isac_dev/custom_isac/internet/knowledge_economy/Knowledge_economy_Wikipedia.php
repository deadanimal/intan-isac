<H2><SPAN class=editsection>[<A title="Edit section: Concepts" 
href="http://en.wikipedia.org/w/index.php?title=Knowledge_economy&amp;action=edit&amp;section=1">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Concepts>Concepts</SPAN></H2>
<P>A key concept of the knowledge economy is that knowledge and education (often 
referred to as "human capital") can be treated as one of the following two:</P>
<UL>
  <LI>A business product, as educational and innovative intellectual products 
  and services can be exported for a high value return. 
  <LI>A productive asset </LI></UL>
<P>It can be defined as</P>
<P><B>" The concept that supports creation of knowledge by organizational 
employees and helps and encourages them to transfer and better utilize their 
knowledge that is in line with company/organization goals "</B></P>
<P><BR>The initial foundation for the Knowledge Economy was first introduced in 
1966 in the book <I>The Effective Executive</I> by Peter Drucker. In this book, 
Drucker described the difference between the manual worker (page 2) and the <A 
title="Knowledge worker" 
href="http://en.wikipedia.org/wiki/Knowledge_worker">knowledge worker</A>. The 
manual worker, according to him, works with his hands and produces goods or 
services. In contrast, a knowledge worker (page 3) works with his or her head 
not hands, and produces ideas, knowledge, and information.</P>
<P>The key problem in the <A class=mw-redirect title=Formalization 
href="http://en.wikipedia.org/wiki/Formalization">formalization</A> and <A 
title="Scientific modelling" 
href="http://en.wikipedia.org/wiki/Scientific_modelling">modeling</A> of 
knowledge economy, is a vague definition of <I><A title=Knowledge 
href="http://en.wikipedia.org/wiki/Knowledge">knowledge</A></I>, which is a 
rather relative concept. For example, it is not proper to consider <I><A 
title="Information society" 
href="http://en.wikipedia.org/wiki/Information_society">information 
society</A></I> as interchangeable with <I>knowledge society</I>. <I><A 
title=Information 
href="http://en.wikipedia.org/wiki/Information">Information</A></I> is usually 
not equivalent to <I>knowledge</I>. Their use, as well, depends on individual 
and group <I><A class=mw-redirect title=Preferences 
href="http://en.wikipedia.org/wiki/Preferences">preferences</A></I> (see the 
cognitive IPK model) – which are "economy-dependent".<SUP class=reference 
id=cite_ref-1><A 
href="http://en.wikipedia.org/wiki/Knowledge_economy#cite_note-1"><SPAN>[</SPAN>2<SPAN>]</SPAN></A></SUP></P>
<H2><SPAN class=editsection>[<A title="Edit section: Driving forces" 
href="http://en.wikipedia.org/w/index.php?title=Knowledge_economy&amp;action=edit&amp;section=2">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Driving_forces>Driving forces</SPAN></H2>
<P>Commentators suggest there are various interlocking driving forces, which are 
changing the rules of business and national competitiveness:</P>
<UL>
  <LI><A title=Globalization 
  href="http://en.wikipedia.org/wiki/Globalization">Globalization</A> — markets 
  and products are more global. 
  <LI><A title="Information technology" 
  href="http://en.wikipedia.org/wiki/Information_technology">Information 
  technology</A>, which is related to next three: 
  <UL>
    <LI><A title=Information 
    href="http://en.wikipedia.org/wiki/Information">Information</A>/<A 
    title=Knowledge href="http://en.wikipedia.org/wiki/Knowledge">Knowledge</A> 
    Intensity — efficient production relies on information and <A 
    title="Trade secret" 
    href="http://en.wikipedia.org/wiki/Trade_secret">know-how</A>; over 70 per 
    cent of workers- in developed economies are information workers; many 
    factory workers use their heads more than their hands. 
    <LI><A class=mw-redirect title="New Media" 
    href="http://en.wikipedia.org/wiki/New_Media">New Media</A> – New media 
    increases the production and distribution of knowledge which in turn, 
    results in <I>collective intelligence</I>. Existing knowledge becomes much 
    easier to access as a result of networked data-bases which promote online 
    interaction between users and producers. 
    <LI><A title="Computer networking" 
    href="http://en.wikipedia.org/wiki/Computer_networking">Computer 
    networking</A> and <A title="Connectivity (computer science)" 
    href="http://en.wikipedia.org/wiki/Connectivity_(computer_science)">Connectivity</A>&nbsp;– 
    developments such as the <A title=Internet 
    href="http://en.wikipedia.org/wiki/Internet">Internet</A> bring the "<A 
    class=mw-redirect title="Global village (Internet)" 
    href="http://en.wikipedia.org/wiki/Global_village_(Internet)">global 
    village</A>" ever nearer. </LI></UL></LI></UL>
<P>As a result, goods and services can be developed, bought, sold, and in many 
cases even delivered over electronic networks.</P>
<P>As regards the applications of any new technology, this depends on how it 
meets economic demand. It can remain dormant or make a commercial breakthrough 
(see <A class=mw-redirect title="Diffusion of innovation" 
href="http://en.wikipedia.org/wiki/Diffusion_of_innovation">diffusion of 
innovation</A>).</P>
<H2><SPAN class=editsection>[<A title="Edit section: Characteristics" 
href="http://en.wikipedia.org/w/index.php?title=Knowledge_economy&amp;action=edit&amp;section=3">edit</A>]</SPAN> 
<SPAN class=mw-headline id=Characteristics>Characteristics</SPAN></H2>
<P>It can be argued that the knowledge economy differs from the traditional 
economy in several key respects:</P>
<UL>
  <LI>The economics are not of scarcity, but rather of abundance. Unlike most 
  resources that become depleted when used, information and knowledge can be 
  shared, and actually grow through application. 
  <LI>The effect of location is either 
  <UL>
    <LI>diminished, in some economic activities: using appropriate technology 
    and methods, virtual <A title=Marketplace 
    href="http://en.wikipedia.org/wiki/Marketplace">marketplaces</A> and <A 
    title="Virtual organization" 
    href="http://en.wikipedia.org/wiki/Virtual_organization">virtual 
    organizations</A> that offer benefits of speed, agility, round the clock 
    operation and global reach can be created. 
    <LI>or, on the contrary, reinforced in some other economic fields, by the 
    creation of <A title="Business cluster" 
    href="http://en.wikipedia.org/wiki/Business_cluster">business clusters</A> 
    around centres of knowledge, such as universities and research centres. 
    However, clusters already existed in pre-knowledge economy times. </LI></UL>
  <LI>Laws, barriers, taxes and ways to measure are difficult to apply solely on 
  a national basis. Knowledge and information "leak" to where demand is highest 
  and the barriers are lowest. 
  <LI>Knowledge enhanced products or services can command price premiums over 
  comparable products with low embedded knowledge or knowledge intensity. 
  <LI>Pricing and value depends heavily on context. Thus the same information or 
  knowledge can have vastly different value to different people, or even to the 
  same person at different times. 
  <LI>Knowledge when locked into systems or processes has higher inherent value 
  than when it can "walk out of the door" in people's heads. 
  <LI>Human capital — competencies — are a key component of value in a 
  knowledge-based company, yet few companies report competency levels in annual 
  reports. In contrast, downsizing is often seen as a positive "cost cutting" 
  measure. 
  <LI>Communication is increasingly being seen as fundamental to knowledge 
  flows. Social structures, cultural context and other factors influencing 
  social relations are therefore of fundamental importance to knowledge 
  economies. </LI></UL>
<P>These characteristics require new ideas and approaches from policy makers, 
managers and knowledge workers.</P>
<P>The knowledge economy has manifold forms in which it may appear but there are 
predictions that the new economy will extend radically, creating a pattern in 
which even ideas will be recognised and identified as a commodity. This 
certainly is not the best time to make any hasty judgment on this contention, 
but considering the very nature of 'knowledge' itself, added to the fact that it 
is the thrust of this new form of economy, there certainly is a clear way 
forward for this notion, though the particulars (i.e. the quantum of the 
revolutionary approach and its applicability and commercial value),remain in the 
speculative realm, as of now.</P>
