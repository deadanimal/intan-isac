          <td width="504" valign="top"><p class="style50">Tuan    boleh menukar maklumat penilaian&nbsp;ISAC tuan dengan membuat penjadualan    semula&nbsp;atau batal secara on-line selewat-lewatnya 3 hari sebelum tarikh    penilaian tersebut dijalankan. Langkah-langkah adalah seperti berikut:</p>
              <ol class="style50">
                <li>Layari Laman Web INTAN, <a href="http://www.intanbk.intan.my/" title="blocked::http://www.intanbk.intan.my/">http://www.intanbk.intan.my</a></li>
                <li>Pilih ikon ISAC</li>
                <li>Pilih ikon ”Permohonan dan Penjadualan    Semula”</li>
                <li>Masukkan No. MyKad</li>
                <li>Masukkan No. MyKad sekali lagi</li>
                <li>Pilih pada <u>Penjadualan    Semula</u> atau <u>Batal</u> </li>
              </ol>
            <p class="style50">Sekiranya telah terlewat&nbsp;3 hari    tersebut, tuan hendaklah menghantar surat atau e-mel secara formal kepada    pihak ISAC. Surat atau e-mel tersebut mesti mengandungi maklumat    tuan,&nbsp;jadual penilaian yang dipilih dan alasan tidak dapat menghadiri    penilaian tersebut serta disokong oleh penyelia tuan. Cara ini membolehkan anda    dari tidak disenarai hitam.</p></td>
