          <td width="18" valign="top"><p class="style43">1.</p></td>
          <td width="504" valign="top"><p class="style43">Saya ingin memohon untuk menduduki    penilaian ISAC. Apakah yang perlu saya lakukan?</p></td>
        </tr>
        <tr>
          <td width="18" valign="top"><p><span class="style45"><span class="style47"><span class="style49"></span></span></span></p></td>
          <td width="504" valign="top"><p class="style43">Jawapan :</p></td>
        </tr>
        <tr>
          <td width="18" valign="top"><p><span class="style45"><span class="style47"><span class="style49"></span></span></span></p></td>
          <td width="504" valign="top"><p class="style50">Dimaklumkan    bahawa bermula 8 Mac 2010 semua permohonan untuk menduduki penilaian ISAC    dibuka secara on-line sahaja. Pemohonan boleh dibuat melalui portal INTAN <a href="http://www.intanbk.intan.my/" title="blocked::http://www.intanbk.intan.my/">http://www.intanbk.intan.my</a>.    Permohonan secara on-line ini membolehkan pemohon memilih masa, tarikh    dan tempat penilaian mengikut kesesuaian masing-masing.</p>
              <p class="style50">Tuan/Puan    dinasihatkan untuk mendapatkan persetujuan daripada penyelia tuan/puan    terlebih dahulu sebelum memohon. Ini bagi mengelakkan sebarang kesulitan yang    timbul selapas permohonan diproses, iaitu seperti tidak mendapat kebenaran    dari penyelia.</p></td>
        </tr>
        <tr>
          <td width="18" valign="top"><p class="style50"><strong>2.</strong></p></td>
          <td width="504" valign="top"><p class="style50"><strong>Apakah kaedah    yang digunakan oleh pihak INTAN dalam memilih permohonan yang akan berjaya    menduduki penilaian ISAC?</strong></p></td>
        </tr>
        <tr>
          <td width="18" valign="top"><p><span class="style45"><span class="style47"><span class="style49"></span></span></span></p></td>
          <td width="504" valign="top"><p class="style50"><strong>Jawapan :</strong> </p></td>
        </tr>
        <tr>
          <td width="18" valign="top"><p><span class="style45"><span class="style47"><span class="style49"></span></span></span></p></td>
          <td width="504" valign="top"><p class="style50">Pada dasarnya, INTAN menggunakan kaedah <em>first come    first served</em> dalam menerima permohonan menduduki penilaian. Melalui    kaedah ini, semua pemohon yang berjaya memilih jadual penilaian sendiri (jika    masih ada kekosongan) adalah dianggap berjaya untuk menduduki penilaian ISAC    tersebut. Walau bagaimanapun, penyaringan tetap akan dilakukan untuk    menyingkirkan permohonan palsu atau bermasalah.</p></td>
        </tr>
        <tr>
          <td width="18" valign="top"><p class="style50"><strong>3.</strong></p></td>
