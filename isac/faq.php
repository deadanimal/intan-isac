          <td width="18" valign="top"><p><span class="style45"><span class="style47"><span class="style49"></span></span></span></p></td>
          <td width="504" valign="top"><p class="style50">Ya,    tuan/puan boleh mengemaskini maklumat profil tuan/puan. Langkah-langkah adalah seperti berikut:</p>
              <ol class="style50">
                <li>Layari Laman Web INTAN, <a href="http://www.intanbk.intan.my/" title="blocked::http://www.intanbk.intan.my/">http://www.intanbk.intan.my</a></li>
                <li>Pilih ikon ISAC</li>
                <li>Pilih ikon ”Permohonan dan Penjadualan    Semula”</li>
                <li>Masukkan No. MyKad</li>
                <li>Masukkan No. MyKad sekali lagi</li>
                <li>Kemaskini profil anda</li>
                <li>Klik butang Kemaskini</li>
              </ol>          </td>
        </tr>
